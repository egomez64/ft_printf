#include "ft_printf.h"

int	ft_printnbr(int nb)
{
	int	i;
	int	len;

	i = 1;
	len = nb;
	if (nb == -2147483647 - 1)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (nb < 0)
	{
		nb *= -1;
		write(1, "-", 1);
		i++;
	}
	if (nb / 10 != 0)
		ft_printnbr(nb / 10);
	ft_printchar(nb % 10 + '0');
	while (len / 10 != 0)
	{
		i++;
		len /= 10;
	}
	return (i);
}
