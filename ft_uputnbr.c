#include "ft_printf.h"

int	ft_uputnbr(unsigned int nb)
{
	int	i;

	i = 0;
	if (nb / 10 != 0)
		i = ft_uputnbr(nb / 10);
	i += ft_printchar(nb % 10 + '0');
	return (i);
}
